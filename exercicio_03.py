#float: é um tipo de dado que representa números de ponto flutuante, ou seja,casas decimais. float(input) = o programa pega o valor em texto e transforma em números decimais.

celsius=float(input("insira a temperatura em celsius: "))

#Para calcular adições em Python, utilize o operador + entre os números que deseja somar. O resultado pode ser exibido usando a função print(). Por exemplo, print(5 + 3) exibirá 8. Além disso, você pode armazenar o resultado em uma variável para uso posterior. 

Fahrenheit = (celsius * 9/5) + 32


#print: é uma função embutida usada para exibir informações na saída padrão, geralmente o console ou terminal.
print ("temperatura em fahrenheit é:", Fahrenheit)