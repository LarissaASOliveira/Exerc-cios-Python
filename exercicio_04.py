#int: int é um tipo de dado que representa números inteiros, ou seja, números que não possuem parte decimal. Eles podem ser positivos ou negativos e são usados em diversas operações matemáticas e lógicas dentro do código. 

n = int(input("Digite um número inteiro: "))

#For i in range: é uma estrutura de controle usada para iterar sobre uma sequência de números
for i in range (1,11):
    print(n , "x" , n*i)
